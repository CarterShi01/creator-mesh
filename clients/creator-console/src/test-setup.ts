// jsdom does not implement window.matchMedia — provide a minimal stub.
// Required by detectSurface() → detectSurfaceKind() which checks (display-mode: standalone).
Object.defineProperty(window, 'matchMedia', {
  writable: true,
  value: (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  }),
})
